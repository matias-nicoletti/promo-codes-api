public class GeoUtils {

	public static final String KILOMETER_UNIT = "KILOMETER";
	public static final String MILE_UNIT = "MILE";
	public static final Double METERS_PER_KILOMETER = 1000d;

	public static double distanceInMeters(String lat1, String lon1, String lat2, String lon2) {
		return distance(Double.parseDouble(lat1), Double.parseDouble(lon1), Double.parseDouble(lat2),
				Double.parseDouble(lon2)) * METERS_PER_KILOMETER;
	}

	public static double distance(double lat1, double lon1, double lat2, double lon2) {
		return distance(lat1, lon1, lat2, lon2, KILOMETER_UNIT);
	}

	public static double distance(double lat1, double lon1, double lat2, double lon2, String unit) {
		double theta = lon1 - lon2;
		double dist = Math.sin(deg2rad(lat1)) * Math.sin(deg2rad(lat2))
				+ Math.cos(deg2rad(lat1)) * Math.cos(deg2rad(lat2)) * Math.cos(deg2rad(theta));
		dist = Math.acos(dist);
		dist = rad2deg(dist);
		dist = dist * 60 * 1.1515;
		if (unit == KILOMETER_UNIT) {
			dist = dist * 1.609344;
		}
		return dist;
	}

	/* ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: */
	/* :: This function converts decimal degrees to radians : */
	/* ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: */
	private static double deg2rad(double deg) {
		return (deg * Math.PI / 180.0);
	}

	/* ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: */
	/* :: This function converts radians to decimal degrees : */
	/* ::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: */
	private static double rad2deg(double rad) {
		return (rad * 180 / Math.PI);
	}

}
